# Print out every prime number between 1 and 1000.
number = range(1,1000)

#From where to where should be iterating???
rango=range(2,number)
# print(rango)
for i in rango:
    if number % i == 0:
        print("It is not prime")
        break
    else:
        print("It is prime")
        break
       